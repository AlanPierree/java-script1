console.log("Trabalhando com variáveis");
// console.log é o output (saída) de informações

const idade = 26;
// const declara uma variável fixa (constante), apenas-leitura, imutável

console.log("Idade");
console.log(idade);
console.log(idade + 2);
console.log(idade - 2);
console.log(idade * 2);
console.log(idade / 2);

// Operações podem ser feitas diretamente no console.log ou declaradas como variável

const idadeSomada = idade + 2;
console.log(idadeSomada);